
package org.wildfly.camel.file;


public class DataHolder{

	private String name;
	private int code;

	public String getName(){
		return name;
	}

	public int getCode(){
		return code;
	}

	public void setName(String theName){
		name = theName;
	}

	public void setCode(int theCode){
		code = theCode;
	}


}
